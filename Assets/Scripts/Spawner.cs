using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.Events;

public class Spawner : MonoBehaviour
{
    [SerializeField] private List<Wave> _waves; // лист волн
    [SerializeField] private Transform _spawnPoint; // точка спавна
    [SerializeField] private Player _player; // цель

    private Wave _currentWave; // текущая волна
    private int _currentWaveNumber; // Номер текущей волны
    private float _timeAfterLastSpawn; // время с последней волны
    private int _spawned; // сколько уже создали врагов

    public event UnityAction AllEnemySpawned;

    private void Start()
    {
        SetWave(_currentWaveNumber);
    }

    private void Update()
    {
        if (_currentWave == null)
            return;

       _timeAfterLastSpawn += Time.deltaTime;

        if(_timeAfterLastSpawn >= _currentWave.Delay)
        {
            InstantiateEnemy();
            _spawned++;
            _timeAfterLastSpawn = 0;
        }
        if(_currentWave.Count <= _spawned)
        {
            if(_waves.Count > _currentWaveNumber +1)
                AllEnemySpawned?.Invoke();

            _currentWave = null;
        }
    }
    private void InstantiateEnemy()
    {
        Enemy enemy = Instantiate(_currentWave.Tamplate, _spawnPoint.position, _spawnPoint.rotation, _spawnPoint).GetComponent<Enemy>();
        enemy.Init(_player);
        enemy.Dying += OnEnemyDying;
    }

    private void SetWave(int index)
    {
        _currentWave = _waves[index];
        Debug.Log("Старт");
    }

    public void NextWave()
    {
        SetWave(++_currentWaveNumber);
        _spawned = 0;
    }

    private void OnEnemyDying(Enemy enemy)
    {
        enemy.Dying -= OnEnemyDying;

        _player.AddMoney(enemy.Reward);
    }
}
[System.Serializable]
public class Wave
{
    public GameObject Tamplate;
    public float Delay;
    public int Count;
}
